package com.salesforce.tooling.entity;

/**
 * Entity that represents the contact.
 */
public class Contact {

    private String name;
    private String email;
    private String phone;

    public String getName() {
        return this.name;
    }

    public String getEmail() {
        return this.email;
    }

    public String getPhone() {
        return this.phone;
    }
}
